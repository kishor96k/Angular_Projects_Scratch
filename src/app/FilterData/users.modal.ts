export class Users {

    id: string;
    firstname: string;
    lastname: string;
    email: string;
    mobile: number;
    salary: number;

    constructor(id: any, firstname: any, lastname: any, email: any, mobile: any, salary: any) {
        this.id = id;
        this.firstname = firstname;
        this.lastname = lastname;
        this.email = email;
        this.mobile = mobile;
        this.salary = salary;
    }



}